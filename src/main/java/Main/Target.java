package Main;

public class Target {

    private String type;

    /**
     * Target constructor
     *
     * @param type represents the essence of target
     */
    public Target(String type) {
        this.type = type;
    }

    /**
     * Return the essence
     *
     * @return type
     */
    public String getType() {
        return type;
    }
    
}
